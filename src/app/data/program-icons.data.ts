export function getProgramIconUrl(slug: string): string | null {
  const simpleIcon = SIMPLE_ICON_MAP[slug];
  if (simpleIcon) {
    return `https://cdn.simpleicons.org/${simpleIcon}`;
  }

  const domain = DOMAIN_ICON_MAP[slug];
  if (domain) {
    return `https://icons.duckduckgo.com/ip3/${domain}.ico`;
  }

  return null;
}
